# Tuple is used to store values using ()
# Tuple can not added/remove a value.Uses less space than a list.
myweek = ("mon", "tue", "wed", "thur", "fri", "sat", "sun")
#Display Thur
print(myweek[3])
#Display sat and sun
print(myweek[5:7])
print(myweek[-2:])
print(myweek.index("wed"))
print(myweek.count("tue"))

